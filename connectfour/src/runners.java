public class runners {
    private int score;
    private String gender;


    public runners(int s, String g){
        score = s;
        gender = g;
    }

    public int getScore(){
        return score;
    }
    public String getGender(){
        return gender;
    }


    public void setScore(int s){
        score = s;
    }
    public void setGender(String g){
        gender = g;
    }


}


