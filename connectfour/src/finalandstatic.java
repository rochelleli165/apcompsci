public class finalandstatic {
    public static void main(String[] args){
        //final and static
        final int x = 3;
        // x cannot equal 4 or 5 or anything else anymore once set set forever
    }

    public static void stuff(){
        System.out.println("Hi");
        //static: does not use or modify any instance of a variable or attributenn
        //object files are non-static
    }
}
