public class frac {
    private int num;
    private int den;

    public frac(int n, int d) {
        num = n;
        den = d;
    }

    public int getNum() {
        return num;
    }

    public int getDen() {
        return den;
    }

    public void setNum(int n) {
        num = n;
    }

    public void setDen(int d) {
        den = d;
    }
}
