/*block commenting
* \" writes a "
* \\ writes a \
* \n skips a line
* \t creates a tab
*
* java naming conventions for variables/methods
* 1. start variable with lowercase letter, $, or _
* 2. if variable name is more than one word, capitalize first letter second word
* 3. NEVER START variable name with a number
* */

public class helloworld
{
    public static void main (String[] args)
    {
        int a = 9;
        int b;
        b = 10;
        //the actual project:
        System.out.println("\"Hello\""); //escape sequence
        System.out.println("    /  \\");
        System.out.println("   /    \\");
        System.out.println("   ______");
        System.out.println("   |    |");
        System.out.println("   |\"hi\"|");
        System.out.println("   |    |");
        System.out.println("   |    |");
        System.out.println("  /|    |\\");
        System.out.println(" / |    | \\");
        System.out.println(" ___________");
        System.out.println( "  ||    ||");
        System.out.println( "  ||    ||");

    }
}
